package com.kamaii.rider.ui.models;

public class GuideModel {

    String appnotificationtextid;
    String state;
    String rider;
    String appnotificationtext;

    public String getAppnotificationtextid() {
        return appnotificationtextid;
    }

    public void setAppnotificationtextid(String appnotificationtextid) {
        this.appnotificationtextid = appnotificationtextid;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getRider() {
        return rider;
    }

    public void setRider(String rider) {
        this.rider = rider;
    }

    public String getAppnotificationtext() {
        return appnotificationtext;
    }

    public void setAppnotificationtext(String appnotificationtext) {
        this.appnotificationtext = appnotificationtext;
    }
}
