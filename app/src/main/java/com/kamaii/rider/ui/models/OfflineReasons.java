package com.kamaii.rider.ui.models;

public class OfflineReasons {

      String id = "";
      String rider_msg= "";
      String status = "";

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRider_msg() {
        return rider_msg;
    }

    public void setRider_msg(String rider_msg) {
        this.rider_msg = rider_msg;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
